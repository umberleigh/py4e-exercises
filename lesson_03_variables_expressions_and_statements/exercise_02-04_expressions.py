# Understanding what some different expressions will do and their resultant types
width = 17
height = 12.0

width // 2  # divide by and floor. Result: 8 (int)
width/2.0  # divide.  Result: 8.5 (float)
height/3  # divide. Result: 4.0 (float)
print(1 + 2 * 5)  # PEMDAS means this is 1 + (2 * 5). Result: 11 (int)
